def get_locations(country_code):
    country = LOCATIONS.get(country_code)
    if not country:
        return {"error": "Bu ülke henüz desteklenmiyor"}
    return {
        "country": country["name"],
        "city": country["city"],
        "areas": country["areas"],
    }


def get_supported_countries():
    return [{"code": k, "name": v["name"]} for k, v in LOCATIONS.items()]


def validate_location(country_code, area_type, area_name):
    country = LOCATIONS.get(country_code)
    if not country:
        return None
    areas = country["areas"].get(area_type)
    if not areas:
        return None
    for area in areas:
        if area["name"] == area_name:
            return {"latitude": area["latitude"], "longitude": area["longitude"]}
    return None